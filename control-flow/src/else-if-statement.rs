fn main() {
  let season = "summer";

  if season == "summer" {
    println!("School is out for the summer!");
  } else if season == "winter" {
    println!("It's cold outside!");
  } else if season == "fall" {
    println!("Leaves are falling!");
  } else if season == "spring" {
    println!("Flowers are blooming!");
  }
}